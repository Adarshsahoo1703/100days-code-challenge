class Solution {
  public:
    long long reversedBits(long long x) {
        // code here
        long long sum=0, i=31;
        while(x)
        {
            long long temp=x&1;
            sum=sum+pow(2,i)*temp;
            i--;
            x=x>>1;
        }
        return sum;
    }
};

